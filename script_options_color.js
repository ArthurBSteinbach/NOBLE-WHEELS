function colorChanger(color) {
    if (color == "wine") {
        document.getElementById("color-text").innerHTML = "Vermelho Metálico Vinho";
    }
    else if (color == "white"){
        document.getElementById("color-text").innerHTML = "Branco Metálico Estelar";
    }
    else if (color == "black"){
        document.getElementById("color-text").innerHTML = "Preto Metálico Infinito"
    }
}